package IfElse_switch.Exercise;

public class Program15 {
    public static void main(String[] args) {
        //Pythagoream triplet or not
        int a=3,b=4,c=5;

        if( ((a*a)+(b*b))==(c*c) ){
            System.out.println("Pythagoream triplet");
        }else{
            System.out.println("Not Pythagoream triplet");
        }
    }
}
