package IfElse_switch.Exercise;

public class Program16 {
    public static void main(String[] args) {
        //Traffic light
        String col = "Red";
        switch(col){
            case "Red":
                    System.out.println("Stop");
                    break;
            case "Green":
                    System.out.println("Go");
                    break;
            default:
                    System.out.println("Wait ");
    }
}
}
