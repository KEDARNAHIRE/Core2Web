package IfElse.ClassCode;

public class Program7 {
    public static void main(String[] args) {
        int x = 3;
        switch (x) {
            case 1:
                System.out.println("ONE");                
                break;
            case 2:
                System.out.println("TWO");
                break;    
            case 4:
                System.out.println("Three");
            default:
                System.out.println("INVALID");
        }
    }
}
